package IntensiveFosterCare.Driver;

/**
 *
 * @author poojitha
 */
public class Driver {
    private String username;
    private String name;
    private String gender;
    private String fosterParent;
    private String healthCamp;
    private String hospitalName;
    private String dropOffStatus;

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getFosterParent() {
        return fosterParent;
    }

    public void setFosterParent(String fosterParent) {
        this.fosterParent = fosterParent;
    }

    public String getHealthCamp() {
        return healthCamp;
    }

    public void setHealthCamp(String healthCamp) {
        this.healthCamp = healthCamp;
    }

    public String getHospitalName() {
        return hospitalName;
    }

    public void setHospitalName(String hospitalName) {
        this.hospitalName = hospitalName;
    }

    public String getDropOffStatus() {
        return dropOffStatus;
    }

    public void setDropOffStatus(String dropOffStatus) {
        this.dropOffStatus = dropOffStatus;
    }
    
      
}
