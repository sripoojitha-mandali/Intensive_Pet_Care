    private void btnFurtherExaminationLabActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFurtherExaminationLabActionPerformed
        // TODO add your handling code here:
        int ipcselectedRow = tblVetPetOwners.getSelectedRow();
        Vet vetSelected = (Vet) tblVetPetOwners.getValueAt(ipcselectedRow, 0);
        for(LabAssistant labAssistant: ipcsystem.getLabAssistantDirectory().getLabAssistantDirectory()) {
            for(Vet vet: ipcsystem.getVetDirectory().getVetDirectory()) {
                if(vet.getIpchospitalName().equals(labAssistant.getIpchospitalName())) {
                    System.out.println("selected vet : " + vetSelected.getIpcuserName() + " " + vetSelected.getIpchealthCamp());
                    ipcsystem.getLabAssistantDirectory().updateLabAssistantInfo(labAssistant, vetSelected.getIpcuserName(), vetSelected.getIpcpetOwner(),vetSelected.getIpcpetType(),vetSelected.getIpchealthCamp());
                    System.out.println("Lab assistant has been assigned");
                }
            }
        }
        JOptionPane.showMessageDialog(this, "Lab assistant has been assigned successfully");
    }//GEN-LAST:event_btnFurtherExaminationLabActionPerformed


